#1st way :Dynamically assigning values
my_dictionary = dict()
my_dictionary['name'] = "Vamsi"
my_dictionary['Age'] = 20
my_dictionary['Country'] ="India"
print(my_dictionary)
print(my_dictionary['name'])
#difference between dictionaries and list is list is ordered while dictionary is unordered
# In list we retrieve data with index and we already know where the data is in the list
#In a dictionary we store data as {key:value} pair and access data  with key
#Both list and dicionary are changable i.e mutable

